/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	int len, i;
	uint8_t u8val;
	uint8_t u8buffer[MAX_PAYLOAD];
	uint16_t u16buffer[8];
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_RTC_Init();
  MX_USART2_UART_Init();
  MX_TSC_Init();
  /* USER CODE BEGIN 2 */
	System_Init();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1)
	{

  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
		if(ByteQueue_Get(&u8val))
		{
			// call packet decoder
			if(PKT_RECEIVED == SerialCom_Decoder(u8val, buffer))
			{
				// packet data is in the buffer
				DbgPrintf("\r\nNew packet arrived");
				// command byte
				switch(buffer[2])
				{
					case TSC_RPTVAL:
						// report TSC readings
						if(buffer[3])
						{
							tsc_rpt_flag = true;
						}
						else
						{
							tsc_rpt_flag = false;
						}
						break;

					default:
						break;
				}
			}
		}
		else if(btn_int_flag)
		{
			// clear int flag
			btn_int_flag = false;
		}
		else if(rtc_int_flag)
		{
			// clear rtc int flag
			rtc_int_flag = false;

			if(tsc_rpt_flag)
			{
				// get TSC values
				Get_TSC_Count(u16buffer, &len);
				// construct payload
				u8val = 0;
				u8buffer[u8val++] = RPT_U16X01;
				for(i = 0; i < len; i++)
				{
					u8buffer[u8val++] = (uint8_t)(u16buffer[i]>>8);
					u8buffer[u8val++] = (uint8_t)(u16buffer[i]&0xff);
				}

				SerialCom_SendPacket(u8buffer, u8val);
				LED_TOGGLE;
			}
		}
	}
  /* USER CODE END 3 */

}

