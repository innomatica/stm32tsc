void Get_TSC_Count(uint16_t* buffer, int *len)
{
	// discharge IO
	HAL_TSC_IODischarge(&htsc, ENABLE);
	// delay for some time
	HAL_Delay(5);
	// select channel if necessary

	// start acquisition
	HAL_TSC_Start(&htsc);

	// wait until the acquisition is done
	while (HAL_TSC_GetState(&htsc) == HAL_TSC_STATE_BUSY)
	{
	}
	// check if the acquisition is valid
	__HAL_TSC_CLEAR_FLAG(&htsc, (TSC_FLAG_EOA | TSC_FLAG_MCE));

	// return the value
	*len = 0;
	buffer[(*len)++] = (uint16_t)HAL_TSC_GroupGetValue(&htsc, TSC_GROUP4_IDX);
	buffer[(*len)++] = (uint16_t)HAL_TSC_GroupGetValue(&htsc, TSC_GROUP5_IDX);
}

